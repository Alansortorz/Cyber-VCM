/* 查询模式 */
#define FACTOR_ADC 0.00080566
float GET_ADC_Value(void)
{
	HAL_ADC_Start(&hadc1);
	HAL_ADC_PollForConversion(&hadc1, 50);
	if(HAL_IS_BIT_SET(HAL_ADC_GetState(&hadc1), HAL_ADC_STATE_REG_EOC)){
		return HAL_ADC_GetValue(&hadc1) * FACTOR_ADC;
	}else
		return 0;
}

/* 中断模式 */
float adc_value[20], _u32Value1=0, _u32Value2=0;
uint8_t n, i;
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	adc_value[n++] = HAL_ADC_GetValue(hadc)*FACTOR_ADC;
	if(n == 20){
		n = 0; 
	}
}

void Get_ADC_Value(float* value)
{
	_u32Value1=0, _u32Value2=0;
	for(i=0; i<20;){
		_u32Value1 += adc_value[i++];
		_u32Value2 += adc_value[i++];
	}
	value[0] = _u32Value1 / 20;
	value[1] = _u32Value2 / 20;
	//printf("%0.3f, %0.3f\n", value[0], value[1]);

}

